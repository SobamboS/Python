student_score = int(input("Student score : "))

if student_score >= 70:
   print("Student Grade is A")
   
elif student_score >= 60 and student_score <= 69:
     print("Student Grade is B")

elif 50<= student_score <= 59:
     print("Student Grade is C")

elif 40 <= student_score <= 49:
     print("Student Grade is D")

elif 30 <= student_score <= 39:
     print("Student Grade is E")

else:
     print("Student Grade is F")
        
